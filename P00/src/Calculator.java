
public class Calculator {
   public int add(int a, int b) {
	   return a + b;
   }
   
   public int subtract(int a, int b) {
	   return a - b;
   }
   public int multi(int a, int b) {
	   return a * b;
   }
   public int divide(int a, int b) {
	   return a / b;
   }
   public double multi2(double a, double b) {
	   return a * b;
   }
}
